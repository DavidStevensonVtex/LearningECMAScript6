console.log("Math.log2(16)    ", Math.log2(16));        // log base 2
console.log("Math.log10(1000))", Math.log10(1000));     // log base 10
console.log("Math.log1p(0))   ", Math.log1p(0));        // same as log(1 + value)
console.log("Math.expm1(0))   ", Math.expm1(0));        // inverse of Math.log1p()
console.log("Math.cbrt(8)     ", Math.cbrt(8));         // cube root of a value

