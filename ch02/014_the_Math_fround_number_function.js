// The Math.fround function rounds a number to a 32-bit floating point value.

console.log("Math.fround(0)     ", Math.fround(0));
console.log("Math.fround(1)     ", Math.fround(1));
console.log("Math.fround(1.137) ", Math.fround(1.137));
console.log("Math.fround(1.5)   ", Math.fround(1.5));

// Math.fround(0)      0
// Math.fround(1)      1
// Math.fround(1.137)  1.1369999647140503
// Math.fround(1.5)    1.5